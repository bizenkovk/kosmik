function renderPolicyText(text: string) {
  const email = conference.supportEmail;
  const parts = text.split(email);

  if (parts.length === 1) {
    return text;
  }

  return parts.map((part, index) => (
    <span key={`${part}-${index}`}>
      {part}
      {index < parts.length - 1 ? (
        <a href={`mailto:${email}`} className="font-semibold text-[#9F1D2D] underline">
          {email}
        </a>
      ) : null}
    </span>
  ));
}

